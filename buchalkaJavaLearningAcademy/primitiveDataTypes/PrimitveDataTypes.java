package buchalkaJavaLearningAcademy.primitiveDataTypes;

public class PrimitveDataTypes {
    public static void main(String[] args) {
        byte myByteMinValue=Byte.MIN_VALUE;
        byte myMaxByteValue=Byte.MAX_VALUE;
        System.out.println("myMinByteValue = " + myByteMinValue);
        System.out.println("myMaxByteValue = " + myMaxByteValue);
        short myMinShortValue=Short.MIN_VALUE;
        short myMaxShortValue=Short.MAX_VALUE;
        System.out.println("myMinShortValue = " + myMinShortValue);
        System.out.println("myMaxShortValue = " + myMaxShortValue);
        int myMinIntValue=Integer.MIN_VALUE;
        int myMaxIntValue=Integer.MAX_VALUE;
        System.out.println("myMaxIntValue = " + myMaxIntValue);
        System.out.println("myMinIntValue = " + myMinIntValue);
        long myMaxLongValue=Long.MAX_VALUE;
        long myLongMinValue=Long.MIN_VALUE;
        System.out.println("myLongMinValue = " + myLongMinValue);
        System.out.println("myMaxLongValue = " + myMaxLongValue);
        float myFloatMaxValue=Float.MAX_VALUE;
        float myFloatMinValue=Float.MIN_VALUE;
        System.out.println("myFloatMinValue = " + myFloatMinValue);
        System.out.println("myFloatMaxValue = " + myFloatMaxValue);
        double myDoubleMinValue= Double.MIN_VALUE;
        double myDoubleMaxValue=Double.MAX_VALUE;
        System.out.println("myDoubleMaxValue = " + myDoubleMaxValue);
        System.out.println("myDoubleMinValue = " + myDoubleMinValue);


    }
}
